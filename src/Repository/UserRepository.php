    /**
     *
     * @return array
     */
    public function getAllUsers()
    {
        $entityManager = $this->getEntityManager();
        $queryBuilder = $entityManager->createQueryBuilder();
        $queryBuilder
            ->select('bp')
            ->from('App:User', 'bp')
            ->orderBy('bp.id', 'DESC');
        return $queryBuilder->getQuery()->getResult();
    }

